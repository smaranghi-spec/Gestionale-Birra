@router.post("/stili/{stile_id}/elimina")
def elimina_stile(stile_id: int, db: Session = Depends(get_db)):
    s = db.query(Stile).filter(Stile.id == stile_id).first()
    if s:
        db.delete(s)
        db.commit()
    return RedirectResponse("/stili?msg=Stile+eliminato", status_code=303)
