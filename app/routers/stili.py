def get_db():
    db = SessionLocal()
    try:
        yield db
    finally:
        db.close()


@router.get("/stili", response_class=HTMLResponse)
def lista_stili(request: Request, msg: str = None, db: Session = Depends(get_db)):
    stili = db.query(Stile).order_by(Stile.linea_guida, Stile.nome).all()
    return templates.TemplateResponse(
        "stili.html",
        {"request": request, "stili": stili, "msg": msg},
    )


@router.post("/stili/popola_bjcp")
def popola_bjcp(db: Session = Depends(get_db)):
    esistenti = {nome for (nome,) in db.query(Stile.nome).all()}
    nuovi = 0

    for row in BJCP_2021:
        (
            nome,
            linea,
            og_min,
            og_max,
            fg_min,
            fg_max,
            ibu_min,
            ibu_max,
            srm_min,
            srm_max,
            ebc_min,
            ebc_max,
            abv_min,
            abv_max,
        ) = row

        if nome not in esistenti:
            db.add(
                Stile(
                    nome=nome,
                    linea_guida=linea,
                    og_min=og_min,
                    og_max=og_max,
                    fg_min=fg_min,
                    fg_max=fg_max,
                    ibu_min=ibu_min,
                    ibu_max=ibu_max,
                    srm_min=srm_min,
                    srm_max=srm_max,
                    ebc_min=ebc_min,
                    ebc_max=ebc_max,
                    abv_min=abv_min,
                    abv_max=abv_max,
                )
            )
            nuovi += 1

    db.commit()
    return RedirectResponse(
        f"/stili?msg=Importati+{nuovi}+stili+BJCP+2021",
        status_code=303,
    )


